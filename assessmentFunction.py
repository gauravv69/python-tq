# Create 4 functions add, sub, mul, div

def add(a,b):
    return a + b

def sub(a,b):
    return a - b

def mul(a,b):
    return a * b

def div(a,b):
    return a / b

x = 10
y = 5

print("Addition:",add(x,y))
print("Substraction:",sub(x,y))
print("Multiplication:",mul(x,y))
print("Division:",div(x,y))

